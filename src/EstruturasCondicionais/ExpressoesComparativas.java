package EstruturasCondicionais;

public class ExpressoesComparativas {
    public static void main(String[] args) {
        int pao = 10;
        int acucar = 20;

        System.out.println(pao == acucar);
        System.out.println(pao != acucar);
        System.out.println(pao > acucar);
        System.out.println(pao < acucar);
        System.out.println(pao >= acucar);
        System.out.println(pao <= acucar);
    }
}
